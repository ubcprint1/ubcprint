export function getOrderById(id: string): Order | undefined {
  return MOCK_ORDERS.find((order) => order.id === id)
}

export function getOrdersByEmployee(employeeId: string): Order[] {
  return MOCK_ORDERS.filter((order) =>
    order.stages.some(
      (stage) => stage.assignedTo === employeeId && (stage.status === "pending" || stage.status === "in_progress"),
    ),
  )
}

export function getOrdersByStage(stage: OrderStage): Order[] {
  return MOCK_ORDERS.filter((order) => order.currentStage === stage)
}
